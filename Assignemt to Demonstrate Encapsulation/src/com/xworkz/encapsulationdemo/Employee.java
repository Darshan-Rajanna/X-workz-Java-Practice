package com.xworkz.encapsulationdemo;

public class Employee {

    // Encapsulation: private fields with public getters and setters
    private String name;
    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
